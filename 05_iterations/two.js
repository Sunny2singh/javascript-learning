// WHILE LOOP

let index = 0
while (index <= 10) {
    //console.log(`value is : ${index}`);
    index = index + 2
}

let myArray = ["flash" , "superman" , "ironman"]
let arr = 0
while ( arr < myArray.length) {
    //console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
}

// DO While Loop

// let score = 11
let score = 1
do {
    console.log(`score is ${score}`);
    score++
} while (score <= 10);
